nama = "Budi"
umur = 25

# Menggunakan operator %
print("Nama saya %s dan saya berumur %d tahun." % (nama, umur))

# Menggunakan metode format()
print("Nama saya {} dan saya berumur {} tahun.".format(nama, umur))

# Menggunakan f-string (cara modern)
print(f"Nama saya {nama} dan saya berumur {umur} tahun.")