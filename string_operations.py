# Penggabungan String
nama_depan = "Andi"
nama_belakang = "Wijaya"
nama_lengkap = nama_depan + " " + nama_belakang
print("Nama Lengkap:", nama_lengkap)

# Pengulangan String
ulang = "Python " * 3
print("Pengulangan:", ulang)

# Mengakses Karakter dalam String (Indexing)
teks = "Python"
print("Huruf pertama:", teks[0])  # P
print("Huruf terakhir:", teks[-1])  # n